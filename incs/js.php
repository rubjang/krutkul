<script src="js/superfish.js"></script>
<script src="js/jquery.toggleformtext.js"></script>
<script src="js/jquery.flexslider.js"></script>
<script src="js/jquery.fancybox.js?v=2.1.5"></script>


<script>
$('#nav-drop').superfish().find('ul');
	$('#nav-drop').superfish({
	animation:{
		opacity:'show',height:'show'
		}, speed:1, dropShadows: false
});

$(document).ready(function(){
	//expand nav
	$('.btn-nv-m .b-ex').click(function(){
		$(this).toggleClass('active');
		$('body').toggleClass('expand');
	});
	/*back to top*/
	$(window).scroll(function(){
		if ($(this).scrollTop() > 100) {
			$('.btn-back2top').css({bottom:"0"});
		} else {
			$('.btn-back2top').css({bottom:"-100px"});
		}
	});
	$('.btn-back2top a').click(function(){
		$('html, body').animate({scrollTop: '0px'}, 200);
		return false;
	});
	/*Lightbox*/
	$('.fancybox').fancybox({
		'titleShow'     : false,
		'transitionIn'	: 'elastic',
		'transitionOut'	: 'elastic',
		'easingIn'      : 'easeOutBack',
		'easingOut'     : 'easeInBack'
	});
}); 
/*FlexSlider*/
$(window).load(function() {
  $('#main-slide').flexslider({
    animation: "slide",
	directionNav: false,
	//slideshow: false,
	//smoothHeight: true,
	slideshowSpeed: 8000, 
  });
});
</script>
